print("ENTER THE OPERATOR YOU WANT TO USE :")
operator=input()
print("ENTER THE TWO NUMBERS")
y=float(input())
z=float(input())

if operator=="+" :
    if y==56 and z==9 :
        print("77")
    else :
        print("ADDITION=",y+z)

if operator=="-" :
    print("subtraction= ",y-z)

if operator=="*" :
    if y==45 and z==3 :
        print("555")
    else :
        print("multiplication= ",y*z)

if operator=="/" :
    if y==6 and z==2 :
        print("34")
    else :
        print("division= ",y/z)





